function init

global userInterface
if isgraphics(userInterface)
    tobiimenu = uimenu(findobj(userInterface, 'Tag', 'importEyeDataMenu'),...
        'Label', 'From &Tobii');
    uimenu(tobiimenu,...
        'Label', 'E&xcel file',...
        'Callback', @(h, e)...
            updateglobals('eyeData',...
                'append',...
                @() pupl_importraw(...
                    'loadfunc', @loadtobii,...
                    'type', 'eye',...
                    'args', {'xls'}),...
                1));
    uimenu(tobiimenu,...
        'Label', '&Tab-delimited (tsv/tdf/txt) file',...
        'Callback', @(h, e)...
            updateglobals('eyeData',...
                'append',...
                @() pupl_importraw(...
                    'loadfunc', @loadtobii,...
                    'type', 'eye',...
                    'args', {'tab'}),...
                1));
end

end